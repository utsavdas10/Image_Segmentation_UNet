# Hyperparameter
LEARNING_RATE = 1e-4
DEVICE = "cuda" if torch.cuda.is_available() else "cpu"
BATCH_SIZE = 16
NUM_EPOCHS = 3
NUM_WORKERS = 2
IMAGE_HEIGHT = 572
IMAGE_WIDTH = 572
PIN_MEMORY = True
LOAD_MODEL = False
TRAIN_IMG_DIR = "C:/Users/utsav/Desktop/UNET2/data/train_images"
TRAIN_MASK_DIR = "C:/Users/utsav/Desktop/UNET2/data/train_masks"
VAL_IMG_DIR = "C:/Users/utsav/Desktop/UNET2/data/test_images"
VAL_MASK_DIR = "C:/Users/utsav/Desktop/UNET2/data/test_masks"

ds_obj = CellDataset(TRAIN_IMG_DIR, TRAIN_MASK_DIR)
xi, yi = ds_obj.__getitem__(0)
